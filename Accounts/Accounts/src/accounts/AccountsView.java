    private void jbtnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCreateActionPerformed
        double sv;
        count = 0;
        try {
            sv = Double.parseDouble(jtxtStartVal.getText());
            
        } catch (NumberFormatException e) {
            sv = 0;
        }
        switch (jtxtTypeCd.getText()) {
            case Savings.TypeCd:
                account = new Savings(jtxtAcctNm.getText(), sv);
                tc = Savings.TypeCd;
                break;
            case Checking.TypeCd:
                account = new Checking(jtxtAcctNm.getText(), sv);
                tc = Checking.TypeCd;
                break;
            case CreditCard.TypeCd:
                account = new CreditCard(jtxtAcctNm.getText(), sv);
                tc = CreditCard.TypeCd;
                break;
            case EquityLine.TypeCd:
                account = new EquityLine(jtxtAcctNm.getText(), sv);
                tc = EquityLine.TypeCd;
                break;
            case MoneyMarket.TypeCd:
                account = new MoneyMarket(jtxtAcctNm.getText(), sv);
                tc = MoneyMarket.TypeCd;
                break;    
        } //end switch
        if (account.getErrMsg().isEmpty()) {
            displayValues();
            statusMessageLabel.setText(account.getActionMsg());
        } else {
            statusMessageLabel.setText(account.getErrMsg());
        }
        setInputLine(false);
        
    }//GEN-LAST:event_jbtnCreateActionPerformed

    private void jbtnChgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnChgActionPerformed
        statusMessageLabel.setText("");
        double amount;
        try {
            amount = Double.parseDouble(jtxtChgAmt.getText());
            
        } catch (NumberFormatException e) {
            statusMessageLabel.setText("non-numeric amount");
            return;
        }        
        
        account.setCharge(tc, jtxtChgDesc.getText(), amount);
        if (account.getErrMsg().isEmpty()) {
            statusMessageLabel.setText(account.getActionMsg());
            displayValues();
            jtxtChgAmt.setText("");
            jtxtChgDesc.setText("");
        } else {
            statusMessageLabel.setText(account.getErrMsg());
        }
        jtxtChgAmt.requestFocusInWindow();
        
        if (account instanceof MoneyMarket) {
            count += 1;
            MoneyMarket mm = new MoneyMarket(Integer.parseInt(jtxtAcctNo.getText()));
            System.out.print(count);
            if (count > 3) {
                account.setCharge(tc, "exceeded withdrawal.", 25);
                if (account.getErrMsg().isEmpty()) {
                   statusMessageLabel.setText("Charge of: "+ (amount + 25) + " for " +
                                              "exceeded withdrawal.");
                   displayValues();                   
                   jtxtChgAmt.setText("");
                   jtxtChgDesc.setText("");
                } else {
                    statusMessageLabel.setText(account.getErrMsg());
                }
               
                mm.writeLogWithd(tc, Integer.parseInt(jtxtAcctNo.getText()));
                
            } else {
                return;
                
            }
                
        }
        
    }//GEN-LAST:event_jbtnChgActionPerformed

