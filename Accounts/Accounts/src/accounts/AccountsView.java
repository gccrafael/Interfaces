    private void jmnuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmnuOpenActionPerformed
        int acctno;
        count = 0;
        statusMessageLabel.setText(" ");
        JFileChooser f = new JFileChooser(".");
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Text Files (*.txt)", "txt");
        f.setFileFilter(filter);
        JDialog dg = new JDialog();
        int rval = f.showOpenDialog(dg);
        if (rval == JFileChooser.CANCEL_OPTION) {
            statusMessageLabel.setText("Open cancelled.");
        } else {
            String fName = f.getSelectedFile().getName();
            String newName = fName.substring(0, fName.length() - 4);
            newName = newName.replaceAll("L", "");
            newName = newName.replaceAll("W", "");
            String type = newName.substring(0, 2).toUpperCase();
            tc = type;
            String numberfield = newName.substring(2, newName.length());
            acctno = Integer.parseInt(numberfield);
            switch (type) {
                case Savings.TypeCd:                    
                    account = new Savings(acctno);
                    break;
                case Checking.TypeCd:
                    account = new Checking(acctno);
                    break;
                case CreditCard.TypeCd:                    
                    account = new CreditCard(acctno);
                    break;
                case EquityLine.TypeCd:                    
                    account = new EquityLine(acctno);
                    break;
                case MoneyMarket.TypeCd:                    
                    account = new MoneyMarket(acctno);
                    break;    
                default:
                    statusMessageLabel.setText("Unknown account type. ");
                    return;
            } // end of switch
            if (account.getErrMsg().isEmpty()) {
                displayValues();
                statusMessageLabel.setText(account.getActionMsg());
            } else {
                statusMessageLabel.setText(account.getErrMsg());
            }
        }
   
    }//GEN-LAST:event_jmnuOpenActionPerformed

