    private void displayValues() {
        NumberFormat curr = NumberFormat.getCurrencyInstance();
        jtxtDisplayName.setText(account.getAcctName());
        jtxtAcctNo.setText(String.valueOf(account.getAcctNo()));
        jtxtAcctTypeDesc.setText(account.getAcctTypeDesc());
        jtxtBalance.setText(curr.format(account.getBalance()));
        
        if (account instanceof AssetAccount) {
            jlblAvailable.setVisible(false);
            jtxtAvailable.setVisible(false);
            jlblCLimit.setVisible(false);
            jtxtCLimit.setVisible(false);
            jlblEqVal.setVisible(false);
            jtxtEqVal.setVisible(false);
        } else if (account instanceof CreditAccount) {
            jlblAvailable.setVisible(true);
            jtxtAvailable.setVisible(true);
            jlblCLimit.setVisible(true);
            jtxtCLimit.setVisible(true);
            jlblEqVal.setVisible(true);
            jtxtEqVal.setVisible(true);
            CreditAccount cr = (CreditAccount) account;
            jtxtCLimit.setText(curr.format(cr.getCreditLimit()));
            jtxtAvailable.setText(curr.format(cr.getAvailableCredit()));
            if (account instanceof EquityLine) {
                EquityLine eqline = (EquityLine) account;
            jtxtEqVal.setText(curr.format(eqline.getEquityValue()));            
        }
        }
    }
    
